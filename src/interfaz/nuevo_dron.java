    private String AsignarFecha() {
      Calendar calendario = Calendar.getInstance();
      int mes = calendario.get(Calendar.MONTH) +1;
      String fecha = calendario.get(Calendar.DATE) +"/"+
                     mes +"/"+
                    calendario.get(Calendar.YEAR);
      return fecha;
    }

    
