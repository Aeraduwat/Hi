    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String equipo = txt_tipo_equipo.getText();
        String marca = txt_marca.getText();
        String num_serie = txt_num_serie.getText();
        String obs = txt_obs.getText();
        String otros = txt_otros.getText();
        String estado = txt_estado.getText();
        String fecha_estado = txt_fecha_estado.getText();

        Integer cant = Integer.parseInt(txt_cantidad.getText());
        Integer adap_bat = Integer.parseInt(cont_adap_bateria.getValue().toString());
        Integer adap = Integer.parseInt(cont_adaptador.getValue().toString());
        Integer bateria = Integer.parseInt(cont_bateria.getValue().toString());
        Integer control = Integer.parseInt(cont_control.getValue().toString());
        Integer enchufe = Integer.parseInt(cont_enchufe.getValue().toString());
        Integer gimball = Integer.parseInt(cont_gimball.getValue().toString());
        Integer helices = Integer.parseInt(cont_helices.getValue().toString());
        Integer helices_r = Integer.parseInt(cont_helices_repuesto.getValue().toString());
        Integer maleta = Integer.parseInt(cont_maleta.getValue().toString());
        Integer prot_helices = Integer.parseInt(cont_protec_helices.getValue().toString());
        Integer usb = Integer.parseInt(cont_cables_usb.getValue().toString());
        Integer cargador = Integer.parseInt(cont_cargador.getValue().toString());

        if (num_serie.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese n° de serie del producto");
        } else if ( lbl_titulo.getText().equals("NUEVO DRON:")) {
            Dron dron = new Dron(equipo, marca, num_serie, cant, helices, gimball, bateria, control, adap, maleta, prot_helices, helices_r, usb, cargador, enchufe, adap_bat, obs, otros, estado,fecha_estado);
            bsn.Agregar(dron);

            if (JOptionPane.showConfirmDialog(this, "Agregar otro Producto?") == JOptionPane.OK_OPTION) {
                Limpiar();
            } else {
                this.dispose();
                inventario = new Inventario();
                inventario.setVisible(true);
            }
        }else{
           Dron dron = new Dron(equipo, marca, num_serie, cant, helices, gimball, bateria, control, adap, maleta, prot_helices, helices_r, usb, cargador, enchufe, adap_bat, obs, otros, estado, fecha_estado);
           bsn.Actualizar(dron);
           this.dispose();
           inventario = new Inventario();
           inventario.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

